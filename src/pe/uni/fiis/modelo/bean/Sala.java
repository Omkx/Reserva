package pe.uni.fiis.modelo.bean;

public class Sala {
    private Integer idSala;
    private Integer nroSala;
    private Integer nroAsientos;

    public Integer getIdSala() {
        return idSala;
    }

    public void setIdSala(Integer idSala) {
        this.idSala = idSala;
    }

    public Integer getNroSala() {
        return nroSala;
    }

    public void setNroSala(Integer nroSala) {
        this.nroSala = nroSala;
    }

    public Integer getNroAsientos() {
        return nroAsientos;
    }

    public void setNroAsientos(Integer nroAsientos) {
        this.nroAsientos = nroAsientos;
    }
}
